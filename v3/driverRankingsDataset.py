import pandas as pd

def avg(ratings):
    return round((sum(ratings)/ len(ratings))/10, 3)

seasonMap = {
    2018: driverRankings2018, 2019: driverRankings2019,
    2020: driverRankings2020, 2021: driverRankings2021,
    2022: driverRankings2022, 2023: driverRankings2023,
    2024: driverRankings2024, 2025: driverRankings2025
}

for seasonDict in seasonMap.values():
    for driver, ratings in seasonDict.items():
        seasonDict[driver] = {"ratings": ratings, "average": avg(ratings)}

df = pd.read_csv("datasets/datasetV3Intial.csv")

df["driverRating"] = df.apply(
    lambda row: seasonMap[row["Season"]][row["Driver"]]["average"], axis = 1
)

df.to_csv("datasets/datasetV3RatingAddition.csv", index = False)